#regular operators
print(10 / 3)

# modulous
print(10 % 3)

# exponent
print(10 ** 4)

# incrementation
x = 10
x += 3  # same as x = x + 3
print(x)

# Order of operation, PEMDAS
x = 10 + 8 * (6-2)
print(x)

# round
x = 2.9
print(round(x))

# Abs
print(abs(-2.9))

# modules
import math

print(math.ceil(x))
print(math.floor(x))
print(math.fabs(x))
